#include "../../include/push_swap.h"

/* Check if character is a digit */
int	ft_isdigit(int c)
{
	return (c >= '0' && c <= '9');
}

/* Check if character is a whitespace */
int	ft_isspace(int c)
{
	return (c == ' ' || c == '\t' || c == '\n' || c == '\v'
		|| c == '\f' || c == '\r');
}
